# comparision operators / Relational operators
a=24
b=13
print(">=",a>=b)
print("<=",a<=b)
print(">",a>b)
print("<",a<b)
print("==",a==b)
print("!=",a!=b)

#logical operators
a=True
b=False
print("and",a and b)
print("or",a or b)
print("not",not b)

#bitwise operators
a=24
b=13
print("bitwise &",a&b)
print("bitwise |",a|b)
print("bitwise ^",a^b)
print("bitwise >>",a>>b)
print("bitwise <<",a<<b)

#identity operators
a=24
